/**
 * Get country by ISO code
 */
export function getCountryByCode(code: string): Country | undefined {
  return countries.find(c => c.code === code);
}

/**
 * Get country by name
 */
export function getCountryByName(name: string): Country | undefined {
  return countries.find(c => c.name.toLowerCase() === name.toLowerCase());
}

/**
 * Get country names array (for simple dropdowns)
 */
export function getCountryNames(): string[] {
  return countries.map(c => c.name);
}

/**
 * Get formatted country string for display
 * Format: "ğŸ‡ºğŸ‡¸ United States"
 */
export function formatCountry(country: Country | string): string {
  if (typeof country === 'string') {
    const found = getCountryByName(country);
    return found ? `${found.flag} ${found.name}` : country;
  }
  return `${country.flag} ${country.name}`;
}

